export function getBlocksForOffice(officeAddress: string): string[] {
  const blocks = officeLocationsData
    .filter((loc) => loc.address === officeAddress)
    .map((loc) => loc.block);
  return Array.from(new Set(blocks));
}

export function getLocationsForBlock(officeAddress: string, block: string): string[] {
  const locations = officeLocationsData
    .filter((loc) => loc.address === officeAddress && loc.block === block)
    .map((loc) => loc.location);
  return Array.from(new Set(locations)).filter((loc) => loc !== '');
}

export function getRoomsForLocation(
  officeAddress: string,
  block: string,
  location: string
): string[] {
  const rooms = officeLocationsData
    .filter(
      (loc) =>
        loc.address === officeAddress &&
        loc.block === block &&
        (loc.location === location || location === '')
    )
    .map((loc) => loc.room);
  return Array.from(new Set(rooms)).filter((room) => room !== '');
}

export function hasLocationsForBlock(officeAddress: string, block: string): boolean {
  return officeLocationsData.some(
    (loc) => loc.address === officeAddress && loc.block === block && loc.location !== ''
  );
}

export function hasRoomsForLocation(
  officeAddress: string,
  block: string,
  location: string
): boolean {
  return officeLocationsData.some(
    (loc) =>
      loc.address === officeAddress &&
      loc.block === block &&
      loc.location === location &&
      loc.room !== ''
  );
}
